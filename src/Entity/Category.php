        return $this->serialize(array(
            $this->id,
            $this->name
        ));
    }

    public function unserialize($serialized) {
        list(
            $this->id,
            $this->name
            ) = unserialize($serialized, array('allowed_classes' => false));
    }
    */

    public function __toString()
    {
        return $this->name;
    }
}
